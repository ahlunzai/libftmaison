int		ft_is_prime(int nb)
{
	int prime;

	if (nb < 0)
		return (0);
	if ((nb == 0) || (nb == 1))
		return (0);
	prime = 2;
	while (prime <= (nb / 2))
	{
		if ((nb % prime) == 0)
			return (0);
		else
			prime++;
	}
	return (1);
}
